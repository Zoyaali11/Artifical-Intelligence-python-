# Bitwise Operators in Python
a = 60  
b = 13 

# Bitwise AND
c = a & b 
print("Line 1", c)

# Bitwise OR
c = a | b  
print("Line 2", c)

# Bitwise XOR
c = a ^ b 
print("Line 3", c)

# Bitwise NOT
c = ~a  
print("Line 4", c)

# Bitwise Left Shift
c = a << 2  
print("Line 5", c)

# Bitwise Right Shift
c = a >> 2   
print("Line 6", c)

